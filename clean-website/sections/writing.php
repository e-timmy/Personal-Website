            <h3>Honours Thesis</h3>
            <p><strong>Mending Memory: An Essay on Technics and Colonialism </strong>
                <br>Abstract: Technology is traditionally conceived as operating according to an instrumental logic,
                efficiency for efficiency’s sake. In this essay, I will demonstrate another side of technology,
                the possessive, by advancing an anticolonial critique. Put simply, I argue that the neoliberal
                individual, state, and their relation are technologies that have become appropriated into the
                telos of technology, Enframing. Accordingly, their ideological constructions, utilised to
                maintain possession, perpetuate the totalising instrumentality of technology. From my critique,
                I make the conclusion that Enframing is dangerously simplifying memory, both culturally and
                ecologically. I also characterise the cosmology behind Enframing, as that which devours other
                cosmologies. To mend memory and dissolve technology’s possessive, through the opening of
                cosmology, I propose a pluralism of technics embedded in an affirmation of the land. Within
                this pluralism, I advance a novel technic of my own, making-anarchic: the making of the
                unmaking of time.</p>
